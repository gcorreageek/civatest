package com.flit.math;

public interface Addition {

	/**
	 * Este método puede sumar dos números infinitamente grandes.
	 * 
	 * Los números a y b son números naturales.
	 * 
	 * @param a Primer número de la suma.
	 * @param b Segundo número de la suma.
	 * @return Suma.
	 */
	String add(String a, String b);
	
}
